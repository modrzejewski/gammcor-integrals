Rd, CntrD, ExpD, NprimD, Kappa)
!
! Two-electron integrals class: (fp|dd)
! L-dependent normalization constants are not applied.
! Output memory layout: CDAB
!
real(F64), dimension(30, *), intent(out) :: G
