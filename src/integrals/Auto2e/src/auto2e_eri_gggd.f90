subroutine auto2e_BraKetTransfer_4_4_4_2(G, W, Ra, Rb, Rc, Rd)
!
! Transform (LS|KS), L=4..8, K=4..6 using the horizontal recurrence relation
! of Eq. 18 in Head-Gordon, M. and Pople, J.A. J. Chem. Phys. 89, 5777 (1988);
! doi: 10.1063/1.455553
!
! The output integrals are expressed in the Cartesian GTOs basis.
!
real(F64), dimension(90, *), intent(out) :: G
real(F64), dimension(145, 64), intent(in) :: W
real(F64), dimension(3), intent(in) :: Ra, Rb, Rc, Rd
real(F64), dimension(3) :: AB, CD
real(F64), dimension(145, 90) :: T
real(F64), dimension(90, 145) :: U
AB = Ra - Rb
CD = Rc - Rd
call auto2e_KetTransfer_4_2(T, W, CD(1), CD(2), CD(3))
U = transpose(T)
call auto2e_KetTransfer_4_4(G(:, 1:225), U, AB(1), AB(2), AB(3))
end subroutine auto2e_BraKetTransfer_4_4_4_2

subroutine auto2e_BraKetTransfer_Spher_4_4_4_2(G, W, Ra, Rb, Rc, Rd)
!
! Transform (LS|KS), L=4..8, K=4..6 using the horizontal recurrence relation
! of Eq. 18 in Head-Gordon, M. and Pople, J.A. J. Chem. Phys. 89, 5777 (1988);
! doi: 10.1063/1.455553
!
! The output integrals are expressed in the real spherical harmonics basis.
!
real(F64), dimension(45, *), intent(out) :: G
real(F64), dimension(145, 64), intent(in) :: W
real(F64), dimension(3), intent(in) :: Ra, Rb, Rc, Rd
real(F64), dimension(3) :: AB, CD
real(F64), dimension(145, 90) :: Txyz
real(F64), dimension(145, 45) :: T
real(F64), dimension(45, 145) :: U
real(F64), dimension(45, 225) :: Gxyz
AB = Ra - Rb
CD = Rc - Rd
call auto2e_KetTransfer_4_2(Txyz, W, CD(1), CD(2), CD(3))
call auto2e_SpherTransf_U_Vector_4_2(T, Txyz, 145)
U = transpose(T)
