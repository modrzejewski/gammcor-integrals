subroutine auto2e_BraKetTransfer_5_3_3_2(G, W, Ra, Rb, Rc, Rd)
!
! Transform (LS|KS), L=5..8, K=3..5 using the horizontal recurrence relation
! of Eq. 18 in Head-Gordon, M. and Pople, J.A. J. Chem. Phys. 89, 5777 (1988);
! doi: 10.1063/1.455553
!
! The output integrals are expressed in the Cartesian GTOs basis.
!
real(F64), dimension(60, *), intent(out) :: G
real(F64), dimension(130, 46), intent(in) :: W
real(F64), dimension(3), intent(in) :: Ra, Rb, Rc, Rd
real(F64), dimension(3) :: AB, CD
real(F64), dimension(130, 60) :: T
real(F64), dimension(60, 130) :: U
AB = Ra - Rb
CD = Rc - Rd
call auto2e_KetTransfer_3_2(T, W, CD(1), CD(2), CD(3))
U = transpose(T)
call auto2e_KetTransfer_5_3(G(:, 1:210), U, AB(1), AB(2), AB(3))
end subroutine auto2e_BraKetTransfer_5_3_3_2

