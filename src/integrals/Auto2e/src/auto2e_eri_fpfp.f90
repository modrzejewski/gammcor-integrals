subroutine auto2e_BraKetTransfer_3_1_3_1(G, W, Ra, Rb, Rc, Rd)
!
! Transform (LS|KS), L=3..4, K=3..4 using the horizontal recurrence relation
! of Eq. 18 in Head-Gordon, M. and Pople, J.A. J. Chem. Phys. 89, 5777 (1988);
! doi: 10.1063/1.455553
!
! The output integrals are expressed in the Cartesian GTOs basis.
!
real(F64), dimension(30, *), intent(out) :: G
real(F64), dimension(25, 25), intent(in) :: W
real(F64), dimension(3), intent(in) :: Ra, Rb, Rc, Rd
real(F64), dimension(3) :: AB, CD
real(F64), dimension(25, 30) :: T
real(F64), dimension(30, 25) :: U
AB = Ra - Rb
CD = Rc - Rd
call auto2e_KetTransfer_3_1(T, W, CD(1), CD(2), CD(3))
U = transpose(T)
call auto2e_KetTransfer_3_1(G(:, 1:30), U, AB(1), AB(2), AB(3))
end subroutine auto2e_BraKetTransfer_3_1_3_1

subroutine auto2e_BraKetTransfer_Spher_3_1_3_1(G, W, Ra, Rb, Rc, Rd)
